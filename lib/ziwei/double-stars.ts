// 缓存双星对
const DOUBLE_STAR_PAIRS: Array<[string, string]> = DOUBLE_STAR_DEFS.map(d => {
  // 解析 "紫微天府" → ["紫微", "天府"]
  // 处理4字名：日月同宫、巨日同宫
  if (d.short === '日月同宫') return ['太阳', '太阴'];
  if (d.short === '巨日同宫') return ['巨门', '太阳'];
  return [d.short.slice(0, 2), d.short.slice(2)];
});

/**
 * 检测命盘中所有双星同宫
 */
export function detectDoubleStars(chart: ZiweiChart): DoubleStar[] {
  const results: DoubleStar[] = [];

  for (let i = 0; i < DOUBLE_STAR_DEFS.length; i++) {
    const def = DOUBLE_STAR_DEFS[i];
    const [star1, star2] = DOUBLE_STAR_PAIRS[i];

    const p1 = findStarPalace(chart, star1);
    const p2 = findStarPalace(chart, star2);
    if (!p1 || !p2) continue;
    if (p1.branch !== p2.branch) continue; // 不同宫

    // 同宫！计算等级
    const palace = p1;
    let level: DoubleStar['level'] = def.base;
    let levelReason: string[] = [];

    // 加吉/加煞调整
    if (hasStar(palace, '禄存')) levelReason.push('+禄存');
    if (getStarSiHua(palace, star1) === '禄' || getStarSiHua(palace, star2) === '禄') {
      levelReason.push('+化禄');
      if (level === 'neutral') level = 'good';
      else if (level === 'caution') level = 'neutral';
    }
    if (getStarSiHua(palace, star1) === '忌' || getStarSiHua(palace, star2) === '忌') {
      levelReason.push('-化忌');
      if (level === 'good') level = 'neutral';
      else if (level === 'neutral') level = 'caution';
      else if (level === 'caution') level = 'bad';
    }
    if (hasShaInPalace(palace, SHA_NAMES)) {
      levelReason.push('-加煞');
      if (level === 'good') level = 'neutral';
      else if (level === 'neutral') level = 'caution';
    }

    // 庙旺调整
    if (isBright(palace, star1) || isBright(palace, star2)) {
      levelReason.push('+庙旺');
    }
    if (isDim(palace, star1) || isDim(palace, star2)) {
      levelReason.push('-落陷');
      if (level === 'good') level = 'neutral';
      else if (level === 'neutral') level = 'caution';
    }

    results.push({
      name: def.name,
      short: def.short,
      level,
      nature: def.nature,
      dingdiao: def.dingdiao,
      description: def.description,
      yiju: def.yiju,
      chuchu: def.chuchu,
      palaces: [palace.name],
      inMingGong: palace.branch === chart.mingGongBranch,
    });
  }

  return results;
}

/**
 * 按宫位查询双星同宫（点击宫位时调用）
 */
export function getDoubleStarsInPalace(chart: ZiweiChart, palaceName: string): DoubleStar[] {
  const palace = chart.palaces.find(p => p.name === palaceName);
  if (!palace) return [];
  const majorStars = palace.stars.filter(s => s.type === 'major').map(s => s.name);

  return detectDoubleStars(chart).filter(ds => {
    return ds.palaces.includes(palaceName);
  });
}
